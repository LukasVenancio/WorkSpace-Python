valueInMeters = int(input('Type the value in meters: '))
valueInCentimeters = valueInMeters * 100
valueInMillimeters = valueInMeters * 1000

print('Value in meters: {}m'.format(valueInMeters))
print('Value in centimeters: {}cm'.format(valueInCentimeters))
print('Value in millimeters: {}mm'.format(valueInMillimeters))
